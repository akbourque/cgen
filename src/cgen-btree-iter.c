#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <ctype.h>
#include "libpstr.h"
#include "cgen_framework.h"

// --- B-Tree Iterator Header Template ---
const char *BTREE_ITER_TEMPLATE_H =
    "#pragma once\n"
    "#ifndef BTREE_ITER_{{KEY_BU}}_{{VAL_BU}}_H\n"
    "#define BTREE_ITER_{{KEY_BU}}_{{VAL_BU}}_H\n\n"
    "#include \"btree_{{KEY}}_{{VAL}}.h\"\n"
    "#include <stdbool.h>\n"
    "#include <stddef.h>\n\n"
    "#define BTREE_ITER_MAX_DEPTH 64\n\n"
    "/**\n"
    " * @brief Ergonomic bundle containing an unpacked entry from a B-Tree node slot.\n"
    " */\n"
    "typedef struct {\n"
    "    {{KEY}} key;\n"
    "    {{VAL}} *value; /**< Mutable reference to allow in-place value modification. */\n"
    "} btree_iter_{{KEY_B}}_{{VAL_B}}_pair_t;\n\n"
    "/**\n"
    " * @brief A single frame tracking state inside our explicit traversal stack.\n"
    " */\n"
    "typedef struct {\n"
    "    const btree_{{KEY_B}}_{{VAL_B}}_node_t *node;\n"
    "    size_t next_child; /**< Index of the next child/key branch to process. */\n"
    "} btree_iter_{{KEY_B}}_{{VAL_B}}_frame_t;\n\n"
    "/**\n"
    " * @brief B-Tree Iterator state tracking mechanical engine.\n"
    " */\n"
    "typedef struct {\n"
    "    const btree_{{KEY_B}}_{{VAL_B}}_t *tree;\n"
    "    btree_iter_{{KEY_B}}_{{VAL_B}}_frame_t stack[BTREE_ITER_MAX_DEPTH];\n"
    "    size_t top; /**< Current stack pointer height depth indicator. */\n"
    "} btree_iter_{{KEY_B}}_{{VAL_B}}_t;\n\n"
    "/**\n"
    " * @brief Allocates and initializes a fresh B-Tree iterator instance.\n"
    " */\n"
    "btree_iter_{{KEY_B}}_{{VAL_B}}_t btree_iter_{{KEY_B}}_{{VAL_B}}_new(const btree_{{KEY_B}}_{{VAL_B}}_t *tree);\n\n"
    "/**\n"
    " * @brief Multiplexed pointer unpacking iterator. Advances state to next in-order element.\n"
    " */\n"
    "bool btree_iter_{{KEY_B}}_{{VAL_B}}_next(btree_iter_{{KEY_B}}_{{VAL_B}}_t *iter, {{KEY}} *out_key, {{VAL}} **out_val);\n\n"
    "/**\n"
    " * @brief Packaged pair structure iterator. Advances state to next in-order element.\n"
    " */\n"
    "bool btree_iter_{{KEY_B}}_{{VAL_B}}_next_pair(btree_iter_{{KEY_B}}_{{VAL_B}}_t *iter, btree_iter_{{KEY_B}}_{{VAL_B}}_pair_t *out_pair);\n\n"
    "#endif\n";

// --- B-Tree Iterator Implementation Template ---
const char *BTREE_ITER_TEMPLATE_C =
    "#include \"btree_iter_{{KEY}}_{{VAL}}.h\"\n"
    "#include <libpstr.h>\n"
    "#include <stdlib.h>\n"
    "#include <assert.h>\n\n"
    "static void push_leftmost_path(btree_iter_{{KEY_B}}_{{VAL_B}}_t *iter, const btree_{{KEY_B}}_{{VAL_B}}_node_t *node) {\n"
    "    while (node != NULL) {\n"
    "        assert(iter->top < BTREE_ITER_MAX_DEPTH && \"B-Tree iterator stack overflow!\");\n"
    "        \n"
    "        iter->stack[iter->top].node = node;\n"
    "        iter->stack[iter->top].next_child = 0;\n"
    "        iter->top++;\n"
    "        \n"
    "        if (node->is_leaf) {\n"
    "            break;\n"
    "        }\n"
    "        node = node->children[0];\n"
    "    }\n"
    "}\n\n"
    "btree_iter_{{KEY_B}}_{{VAL_B}}_t btree_iter_{{KEY_B}}_{{VAL_B}}_new(const btree_{{KEY_B}}_{{VAL_B}}_t *tree) {\n"
    "    btree_iter_{{KEY_B}}_{{VAL_B}}_t iter;\n"
    "    iter.tree = tree;\n"
    "    iter.top = 0;\n"
    "    \n"
    "    if (tree != NULL && tree->root != NULL) {\n"
    "        push_leftmost_path(&iter, tree->root);\n"
    "    }\n"
    "    return iter;\n"
    "}\n\n"
    "bool btree_iter_{{KEY_B}}_{{VAL_B}}_next(btree_iter_{{KEY_B}}_{{VAL_B}}_t *iter, {{KEY}} *out_key, {{VAL}} **out_val) {\n"
    "    while (iter->top > 0) {\n"
    "        btree_iter_{{KEY_B}}_{{VAL_B}}_frame_t *top = &iter->stack[iter->top - 1];\n"
    "        const btree_{{KEY_B}}_{{VAL_B}}_node_t *node = top->node;\n"
    "        \n"
    "        if (node->is_leaf) {\n"
    "            if (top->next_child < node->num_keys) {\n"
    "                size_t idx = top->next_child;\n"
    "                if (out_key) *out_key = node->keys[idx];\n"
    "                if (out_val) *out_val = ({{VAL}} *)&node->vals[idx];\n"
    "                \n"
    "                top->next_child++;\n"
    "                \n"
    "                if (top->next_child >= node->num_keys) {\n"
    "                    iter->top--;\n"
    "                }\n"
    "                return true;\n"
    "            }\n"
    "        } else {\n"
    "            if (top->next_child < node->num_keys) {\n"
    "                size_t idx = top->next_child;\n"
    "                if (out_key) *out_key = node->keys[idx];\n"
    "                if (out_val) *out_val = ({{VAL}} *)&node->vals[idx];\n"
    "                \n"
    "                top->next_child++;\n"
    "                push_leftmost_path(iter, node->children[idx + 1]);\n"
    "                return true;\n"
    "            } else {\n"
    "                iter->top--;\n"
    "            }\n"
    "        }\n"
    "    }\n"
    "    return false;\n"
    "}\n\n"
    "bool btree_iter_{{KEY_B}}_{{VAL_B}}_next_pair(btree_iter_{{KEY_B}}_{{VAL_B}}_t *iter, btree_iter_{{KEY_B}}_{{VAL_B}}_pair_t *out_pair) {\n"
    "    {{KEY}} key;\n"
    "    {{VAL}} *val;\n"
    "    if (btree_iter_{{KEY_B}}_{{VAL_B}}_next(iter, &key, &val)) {\n"
    "        if (out_pair) {\n"
    "            out_pair->key = key;\n"
    "            out_pair->value = val;\n"
    "        }\n"
    "        return true;\n"
    "        }\n"
    "    return false;\n"
    "}\n";

int main(int argc, char **argv) {
    cgen_app_dual_def_t app = {
        .subcommand_name = "btree_iter",
        .template_h      = BTREE_ITER_TEMPLATE_H,
        .template_c      = BTREE_ITER_TEMPLATE_C
    };
    return cgen_app_run_dual(&app, argc - 1, argv + 1);
}
