int main(int argc, char **argv) {
    cgen_app_dual_def_t app = {
        .subcommand_name = "btree",
        .template_h      = BTREE_TEMPLATE_H,
        .template_c      = BTREE_TEMPLATE_C
    };
    return cgen_app_run_dual(&app, argc, argv);
}
